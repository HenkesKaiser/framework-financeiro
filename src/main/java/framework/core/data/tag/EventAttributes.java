package framework.core.data.tag;

public class EventAttributes
{

	public String onblur; // (14) Element loses focus
	public String onchange; // (11) Element’s value changes
	public String onclick; // (17) Mouse button is clicked over the element
	public String ondblclick; // (18) Mouse button is double-clicked over the
								// element
	public String onfocus;// (14) Element receives focus
	public String onkeydown; // (18) Key is pressed
	public String onkeypress; // (18) Key is pressed and subsequently released
	public String onkeyup; // (18) Key is released
	public String onmousedown; // (18) Mouse button is pressed over the element
	public String onmousemove;// (18) Mouse moves over the element
	public String onmouseout;// (18) Mouse leaves the element’s area
	public String onmouseover;// (18) Mouse moves onto an element
	public String onmouseup;// (18) Mouse button is released
	public String onreset;// (1) Form is reset
	public String onselect;// (11) Text is selected in an input field
	public String onsubmit;// (1) Form is submitted

}
