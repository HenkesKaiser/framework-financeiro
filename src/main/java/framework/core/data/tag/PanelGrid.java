package framework.core.data.tag;

public class PanelGrid
{

	public String bgcolor;// Background color for the table
	public String border; // Width of the table’s border
	public String cellpadding;// Padding around table cells
	public String cellspacing;// Spacing between table cells
	public String columnClasses; // Comma-separated list of CSS classes for
									// columns
	public String columns;// Number of columns in the table
	public String footerClass;// CSS class for the table footer
	public String frame;// frame Specification for sides of the frame
						// surrounding the table that are to be drawn; valid
						// values: none, above, below, hsides, vsides, lhs, rhs,
						// box, border
	public String headerClass; // CSS class for the table header
	public String rowClasses; // Comma-separated list of CSS classes for columns
	public String rules;// Specification for lines drawn between cells; valid
						// values: groups, rows, columns, all
	public String summary;// Summary of the table’s purpose and structure used
							// for non-visual feedback such as speech
}
